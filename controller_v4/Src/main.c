/*
 *  main.c
 *
 *  Created on: Mar 5, 2020
 *      Author: jolen
 *
 *  The motor controller (MC) software organizes itself in 4 states:
 *  	1. Initialization (no LED)
 *  	2. Idle (BLUE LED)
 *  	3. Enable (GREEN LED)
 *  	4. Fault (RED LED)
 *
 *  When the MC first turns on it is in the initialization state. After it finishes initializing
 *  the clocks, peripherals, timers, control system, etc. it changes to the idle state. The
 *  control system and PWM signals are off. If the user switches the enable switch from low to
 *  high the MC goes into the enable state. The PWM switches turn on and the control system 16kHz
 *  interrupt turns on. The motor controller can now spin the motor. If a fault is detected at
 *  any time, the MC goes into the fault state. To exit the fault state the user needs to power
 *  cycle the MC.
 *
 *  To identify what fault occurred, the user can either be in debug mode and look into
 *  mc_vars.fault_reg1 or open up the GUI on the visualization page, start live logging, and
 *  look under the fault tab.
 *
 *  The software was written using HAL libraries using the .ioc file in STM32CubeMX and
 *  STM32CubeIDE. When writing software in the main.c, stm32F7xx_it.c, and other stm32 made source
 *  files, make sure you write within the USER CODE BEGIN and USER CODE END. If you write outside
 *  of this and rebuild in the .ioc file, then the user code outside of the USER CODE BEGIN and
 *  USER CODE END will be erased.
 *
 *  If you do rebuilt the .ioc file, make sure you remove the MX_IWDG_Init() under "Initialize
 *  all configured peripherals" at the start of the main function. This is initialized after the
 *  control system init because the control system init takes a really long time and trips the IWDG.
 *  The IWDG init takes place after the control system init for that reason.
 */
